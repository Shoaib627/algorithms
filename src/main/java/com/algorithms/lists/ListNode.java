package com.algorithms.lists;

public class ListNode {

	public int val;

	public ListNode next;
	
	

	public ListNode() {
		
	}

	public ListNode(int val) {
		super();
		this.val = val;
	}

	public int getVal() {
		return val;
	}

	public void setVal(Integer val) {
		this.val = val;
	}

	public ListNode getNext() {
		return next;
	}

	public void setNext(ListNode next) {
		this.next = next;
	}
}